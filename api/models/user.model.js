import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamp: true } //to store user input timeline
);

// Initiating Model
const User = mongoose.model("User", userSchema);

// Exporting the Schema
export default User;
